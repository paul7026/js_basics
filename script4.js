function sum (num1, num2) {
    return num1 + num2;
}


/**
 * функция вычитает одно число из другого 
 * @param {number} num1 первое число 
 * @param {number} num2 второе число
 */
function dif (num1, num2) {
    return num1 - num2;
}

function mult (num1, num2) {
    return num1 * num2;
}

function div (num1, num2) {
    return num1 / num2;
}


console.log(sum(5,6));
console.log(dif(10, 7));
console.log(mult(5,4));
console.log(div(100, 50));
